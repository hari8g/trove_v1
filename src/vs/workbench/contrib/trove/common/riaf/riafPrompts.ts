const buildIndexSnapshotBlock = (snapshot: RiafIndexSnapshot): string => {
	const lines: string[] = [
		'<repository_index_snapshot>',
		`Languages: ${snapshot.languageStack.join(', ') || 'unknown'}`,
		`Frameworks: ${snapshot.frameworks.join(', ') || 'none detected'}`,
		`Package managers: ${snapshot.packageManagers.join(', ') || 'none'}`,
		`Files: ${snapshot.fileCount.toLocaleString()} · LOC: ${snapshot.totalLoc.toLocaleString()} · Indexed chunks: ${snapshot.chunkCount.toLocaleString()}`,
	];

	if (snapshot.buildCommands.length) {
		lines.push(`Build commands: ${snapshot.buildCommands.join(' | ')}`);
	}
	if (snapshot.testCommands.length) {
		lines.push(`Test commands: ${snapshot.testCommands.join(' | ')}`);
	}
	if (snapshot.projectPurpose) {
		lines.push(`Purpose (LLM-generated): ${snapshot.projectPurpose}`);
	}
	if (snapshot.architectureSummary) {
		lines.push(`Architecture (LLM-generated): ${snapshot.architectureSummary}`);
	}
	if (snapshot.serviceNames.length) {
		lines.push(`Spring services detected (${snapshot.serviceNames.length}): ${snapshot.serviceNames.slice(0, 12).join(', ')}${snapshot.serviceNames.length > 12 ? ', …' : ''}`);
		lines.push(`Total REST endpoints indexed: ${snapshot.totalEndpoints}`);
	}
	if (snapshot.pomCount) {
		lines.push(`Maven pom.xml files: ${snapshot.pomCount}`);
	}
	if (snapshot.k8sResourceCount) {
		lines.push(`Kubernetes resources indexed: ${snapshot.k8sResourceCount}`);
	}
	if (snapshot.pipelineJobCount) {
		lines.push(`GitLab CI jobs indexed: ${snapshot.pipelineJobCount}`);
	}
	if (snapshot.sharedNpmPackages.length) {
		lines.push(`Shared NPM packages: ${snapshot.sharedNpmPackages.join(', ')}`);
	}
	lines.push('</repository_index_snapshot>');
	return lines.join('\n');
};

export const buildRiafAgentPrompt = (
	workspaceRoot: string,
	repoTitle: string,
	config: RiafConfig,
	snapshot?: RiafIndexSnapshot,
): string => {
	const outputPath = `${workspaceRoot}/${config.outputFileName}`;
	const testClause = config.includeTests
		? 'Include test files in your analysis.'
		: 'You may skip test files (*.test.ts, *.spec.ts, __tests__/) unless they reveal important patterns.';

	const snapshotBlock = snapshot
		? `\n\nThe repository has already been indexed. Use this pre-computed summary to skip basic discovery — jump directly to deep structural reading:\n\n${buildIndexSnapshotBlock(snapshot)}\n`
		: '';

	const hasIndex = snapshot && snapshot.chunkCount > 0;
	const readFileCap = hasIndex ? 30 : 50;
	const searchCallMin = hasIndex ? 15 : 5;

	return `\
You are performing a full repository analysis to generate a comprehensive \
context document that will be used as the primary context source for an \
AI coding assistant.

## Your Task

Analyse the repository at: ${workspaceRoot}

Then write the completed document to: ${outputPath}
${snapshotBlock}
## Tool Budget

You have **100 read-only tool calls** for exploration. Spend them efficiently:

| Category | Tool(s) | Recommended calls |
|---|---|---|
| Index search (highest signal-per-call) | search_symbols, search_codebase | at least ${searchCallMin} |
| File outline (before reading large files) | get_file_outline | as many as needed |
| Full file read (for key files only) | read_file | max ${readFileCap} |
| Directory navigation | get_dir_tree, ls_dir, search_for_files | max 20 |

**Budget rules:**
- Use search_symbols FIRST to locate service interfaces, DI registrations, and entry points. Each call surfaces up to 50 symbols across the repo — far more efficient than reading directories.
- Use search_codebase to trace specific patterns (IPC channel names, decorator usage, data flow across files) rather than reading whole files.
- Use get_file_outline on any file over 100 lines before deciding whether to read_file it. This costs 1 call and tells you all function/class names.
- Reserve read_file for the ${readFileCap} most architecturally critical files you found through search.
- do NOT spend more than 20 calls on directory browsing (get_dir_tree / ls_dir / search_for_files). ${hasIndex ? 'The index snapshot above already tells you the language stack, frameworks, and commands — skip that part of Phase 1.' : ''}

## How to Execute This

Work through these phases:

**Phase 1 — Structure discovery (max 20 calls)**
${hasIndex
		? `The index snapshot above already provides language stack, frameworks, file count, LOC, build/test commands${snapshot?.serviceNames.length ? ', and service topology' : ''}. Skip discovering those and start with Phase 2 immediately. Only use get_dir_tree / ls_dir if you need to understand directory layout of a specific unfamiliar area.`
		: `Use get_dir_tree on the workspace root to understand the overall layout.
Use ls_dir on each major subdirectory to get detail.
Use search_for_files to locate key files (package.json, tsconfig.json, README.md, main entry points, contribution/registration files).`}

**Phase 2 — Deep index-driven reading (at least ${searchCallMin} search calls, then up to ${readFileCap} read_file calls)**

Start with search tools — they cover the whole indexed codebase in one call each:
1. search_symbols("interface I") — find all service interface definitions
2. search_symbols("registerSingleton OR registerWorkbenchContribution") — find DI registration entry points
3. search_symbols("constructor") — identify main service classes
4. search_codebase("channel listen") — trace IPC call patterns
5. search_codebase("export default OR export const OR export function") — find key exports per subsystem
6. For each important symbol found: call get_file_outline(filePath) to see its full structure
7. Then read_file only the ${readFileCap} most important files

${snapshot?.serviceNames.length ? `For the detected Spring services, also use:
- query_service_topology to get the full inter-service topology
- get_maven_impact("staas-parent" or shared lib artifactId) for dependency blast radius
` : ''}Use search_codebase to trace specific patterns (IPC channels, service registration, tool definitions, data flow).
${testClause}

Read up to ${config.maxFiles} files total — but prefer search tools over reading. You do NOT need to read every file — use search to find the 20–30 most important ones, then read those.

**Phase 3 — Write the document**
Once you have sufficient understanding, write the completed document.
Do not ask for confirmation before writing — just write it.
If ${config.outputFileName} does not exist yet, call create_file_or_folder to create it,
then call rewrite_file with the full markdown content. If it already exists, use
rewrite_file with the complete updated document.

## Output Format

Fill in the template below exactly. Do not add, rename, or remove sections.
Replace every [...] block with real content derived from what you read.
Every sentence must be specific to THIS codebase — zero generic boilerplate.
Section 4 (Module Wiring) and Section 8 (Cookbook) are the most important —
spend the most effort on those two.

${buildContextDocTemplate(config.outputFileName)}`;
};
